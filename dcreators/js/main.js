$(document).ready(function(){
  $(window).scroll(function(){
  	var scroll = $(window).scrollTop();
	  if (scroll > 100) {
	 		$("#navbar").addClass("floatingNav");
	  	  
	   		$(".navbar-header a").removeClass("navbar-brand");
	   		$(".navbar-header a").addClass("small_brand");
	  }

	  else{
 			$('#navbar').removeClass('floatingNav');
		    $(".navbar-header a").removeClass("small_brand");
	    	$(".navbar-header a").addClass("navbar-brand");
		
	  }
  });

  $("#dropdown_mob").click(function(){
  	$(".menu").toggleClass("block");
  });
})


